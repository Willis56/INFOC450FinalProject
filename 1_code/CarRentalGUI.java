    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        ArrayList<Customer> customers = DatabaseManager.getAllCustomers();
        StringBuilder customerList = new StringBuilder();
        for (Customer customer : customers) {
            customerList.append(customer).append("\n");
        }
        jTextArea3.setText(customerList.length() > 0 ? customerList.toString() : "No customers found.");

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            int customerID = Integer.parseInt(jTextField2.getText().trim());
            Customer selectedCustomer = DatabaseManager.getCustomerByID(customerID);
            if (selectedCustomer != null) {
                currentCustomer = selectedCustomer;
                currentCustomerID = customerID;
                JOptionPane.showMessageDialog(this, "Selected Customer: " + currentCustomer);
            } else {
                JOptionPane.showMessageDialog(this, "Customer with ID " + customerID + " not found.");
                currentCustomer = null;
                currentCustomerID = -1;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid Customer ID in the text field.");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void displayAvailableCars() {

        ArrayList<Car> cars = DatabaseManager.getAvailableCars();
        StringBuilder carList = new StringBuilder();
        for (Car car : cars) {
            carList.append(car).append("\n");
        }
        jTextArea1.setText(carList.length() > 0 ? carList.toString() : "No available cars found.");
    }

