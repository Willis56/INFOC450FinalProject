    private void CheckoutButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CheckoutButtonActionPerformed

        if (currentCustomer == null) {
            JOptionPane.showMessageDialog(this, "Need Customer for Checkout");
            return;
        }

        try {
            int rentalDays = Integer.parseInt(DaysText.getText());
            int carID = Integer.parseInt(carIDText.getText());

            double totalCost = rentalSystem.calculateTotal(carID, rentalDays);
            if (totalCost > 0 && rentalSystem.rentCar(carID)) {
                JOptionPane.showMessageDialog(this, "Rental Successful! Total cost: $" + totalCost);
            } else {
                JOptionPane.showMessageDialog(this, "Car not available.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter valid numbers.");
        }

    }//GEN-LAST:event_CheckoutButtonActionPerformed

    private void CreateCustomerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateCustomerButtonActionPerformed
        String CustomerName = nameField.getText();
        String license = licenseField.getText();
        String phone = phoneField.getText();
        currentCustomer = new Customer(CustomerName, license, phone);
        currentCustomerID = DatabaseManager.addCustomer(currentCustomer);
        if (currentCustomerID != -1) {
            JOptionPane.showMessageDialog(this, "Customer Created with ID " + currentCustomerID + ": " + currentCustomer);
        } else {
            JOptionPane.showMessageDialog(this, "Failed to create customer.");
            currentCustomer = null;
        }


    }//GEN-LAST:event_CreateCustomerButtonActionPerformed

